"""
+, -, *, /, //, **, %, ()
"""

# Básicos

print(10 + 2) # ou
print('Adição + ', 10 + 10) # ou para ficar mais explicativo pode usar como assim
print('Subtração - ', 10 - 5)
print('Multiplicação * ', 10 * 10)
print('Divisão / ', 10 / 2)

# Concatenação
print('10' + '2') # ou
print('Concatenação + ', '10' + '2')

# Repetição
print(10 * 'Maria') # ou
print('Repetição * ', 10 * 'Maria') 

# Outros Operadores Aritméticos
print('Divisão Inteira / ', 10 // 3)
print('Potenciação / ', 2 ** 10)
print('Resto da Divisão ou seja Módulo / ', 10 % 5) # ou
print('Resto da Divisão ou seja Módulo / ', 10 % 3)

print(5+2*10) 
print('Alterar a precedência / ', (5+2)*10)
